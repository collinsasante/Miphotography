/**
 * Uploads all images from Miphotographer/ to Cloudinary under the
 * "miphotography/portfolio" folder, then writes src/lib/data/portfolio.ts
 * with the resulting public IDs.
 *
 * Usage: node scripts/upload-portfolio.mjs
 */
import { v2 as cloudinary } from "cloudinary";
import { readFileSync, readdirSync, writeFileSync } from "fs";
import { join, extname } from "path";
import { fileURLToPath } from "url";
import { dirname } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// Load env from .env.local manually
const envPath = join(ROOT, ".env.local");
const env = readFileSync(envPath, "utf8");
for (const line of env.split("\n")) {
  const match = line.match(/^([^#=]+)=(.*)$/);
  if (match) {
    const key = match[1].trim();
    const val = match[2].trim().replace(/^["']|["']$/g, "");
    process.env[key] = val;
  }
}

cloudinary.config({
  cloud_name: process.env.NEXT_PUBLIC_CLOUDINARY_CLOUD_NAME,
  api_key:    process.env.CLOUDINARY_API_KEY,
  api_secret: process.env.CLOUDINARY_API_SECRET,
});

const IMAGE_DIR = join(ROOT, "Miphotographer");
const OUT_FILE  = join(ROOT, "src/lib/data/portfolio.ts");
const FOLDER    = "miphotography/portfolio";

const files = readdirSync(IMAGE_DIR)
  .filter((f) => [".jpg", ".jpeg", ".png", ".webp"].includes(extname(f).toLowerCase()))
  .sort();

console.log(`Found ${files.length} images. Starting upload to Cloudinary…\n`);

const publicIds = [];
let done = 0;

async function uploadBatch(batch) {
  await Promise.all(
    batch.map(async (file) => {
      const filePath = join(IMAGE_DIR, file);
      const publicId = `${FOLDER}/${file.replace(/\.[^.]+$/, "")}`;
      try {
        const result = await cloudinary.uploader.upload(filePath, {
          public_id:     publicId,
          overwrite:     false,
          resource_type: "image",
          quality:       "auto",
          fetch_format:  "auto",
        });
        publicIds.push(result.public_id);
        done++;
        process.stdout.write(`\r  Uploaded ${done}/${files.length}: ${result.public_id}`);
      } catch (err) {
        // If already exists (overwrite: false returns an error), still record it
        if (err.http_code === 400 || err.message?.includes("already exists")) {
          publicIds.push(publicId);
          done++;
          process.stdout.write(`\r  Skipped (exists) ${done}/${files.length}: ${publicId}`);
        } else {
          console.error(`\nFailed: ${file}`, err.message);
        }
      }
    })
  );
}

// Upload in batches of 5 to avoid rate limits
const BATCH = 5;
for (let i = 0; i < files.length; i += BATCH) {
  await uploadBatch(files.slice(i, i + BATCH));
}

// Sort by filename order
publicIds.sort();

// Write the portfolio data file
const ts = `// Auto-generated by scripts/upload-portfolio.mjs
// ${publicIds.length} images hosted on Cloudinary

export interface PortfolioPhoto {
  publicId: string;
}

export const PORTFOLIO_PHOTOS: PortfolioPhoto[] = ${JSON.stringify(
  publicIds.map((id) => ({ publicId: id })),
  null,
  2
)};

/** First N photos for homepage preview */
export const PORTFOLIO_PREVIEW: PortfolioPhoto[] = PORTFOLIO_PHOTOS.slice(0, 12);
`;

writeFileSync(OUT_FILE, ts, "utf8");
console.log(`\n\nDone! Wrote ${publicIds.length} public IDs to src/lib/data/portfolio.ts`);
